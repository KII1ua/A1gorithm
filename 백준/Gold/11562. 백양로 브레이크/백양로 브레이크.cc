int N, M, K;
int graph[251][251];

void Print() {
    for(int i = 1; i <= N; i++) {
        for(int j = 1; j <= N; j++) {
            cout << graph[i][j] << " ";
        }
        cout << endl;
    }
}

void solve() {
    for(int k = 1; k <= N; k++) {
        for(int i = 1; i <= N; i++) {
            for(int j = 1; j <= N; j++) {
                graph[i][j] = min(graph[i][j], graph[i][k] + graph[k][j]);
            }
        }
    }
}

void Init() {
    for(int i = 1; i <= N; i++) {
        for(int j = 1; j <= N; j++) {
            if(i == j) continue;
            graph[i][j] = MAX;
        }
    }
}

void input() {
    cin >> N >> M;

    Init();

    for(int i = 0; i < M; i++) {
        int a, b, c;
        cin >> a >> b >> c;

        graph[a][b] = 0;
        if(c == 0) {
            graph[b][a] = 1;
        }
        else {
            graph[b][a] = 0;
        }
    }
    solve();

    cin >> K;

    for(int i = 0; i < K; i++) {
        int a, b;
        cin >> a >> b;
        cout << graph[a][b] << "\n";
    }
}

